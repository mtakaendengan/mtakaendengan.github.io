// Function to draw matrix on canvas
function drawMatrixOnCanvas() {
    //Define Local Variables
    var M = arguments[0];
	var can = arguments[1];
	var cx = can.getContext("2d");
	var s = can.height / M.length;
	var colorScheme = arguments[2];

    for(var i = 0; i < M.length; i++) {
        for(var j = 0; j < M[i].length; j++) {
            var x = j * s;
            var y = i * s;
            var cR, cG, cB;
            if(colorScheme == 0) {
                switch(M[i][j]) {
                        case 0:
                            cR = 236;
                            cG = 240;
                            cB = 241;
                            //rgb(236, 240, 241)
                        break;
                        case 1:
                            cR = 44;
                            cG = 62;
                            cB = 80;
                            //rgb(44, 62, 80)
                        break;
                        case 3:
                            cR = 255; //231;
                            cG = 0; //76;
                            cB = 0; //60;
                            //rgb(231, 76, 60)
                        break;
                        default:
                }
				cx.beginPath();
				if(M[i][j] < 3) {
					cx.fillStyle =
                    "rgb(" + cR + "," + cG + "," + cB + ")";
					cx.fillRect(x, y, s, s);
					cx.stroke();
				} else {
					cx.fillStyle = "#fdd";
					cx.fillRect(x, y, s, s);
					cx.stroke();
					cx.strokeStyle =
                    "rgb(" + cR + "," + cG + "," + cB + ")";
					cx.lineWidth = 2;
					cx.strokeRect(x+2, y+2, s-4, s-4);
				}
				cx.closePath();
            } else if(colorScheme == 1) {
				var k = M[i][j];
                
				//        OR  NO  NE   EA  SE   SO  SW   WE  NW
				var sx = [+0, +0, +.7, +1, +.7, +0, -.7, -1, -.7];
				var sy = [+0, +1, +.7, +0, -.7, -1, -.7, +0, +.7];

				var xx = x + 0.5 * s;
				var yy = y + 0.5 * s;
				var dx = sx[k] * 0.5 * s;
				var dy = sy[k] * 0.5 * s;

				cx.beginPath();
				cx.moveTo(xx, yy);
				cx.lineTo(xx + dx , yy - dy);
				cx.lineWidth = 2;
				cx.strokeStyle = "#2980b9";
				cx.stroke();

				cx.beginPath();
				cx.arc(xx, yy, 1, 0, 2 * Math.PI);
				cx.lineWidth = 2;
				cx.strokeStyle = "#c0392b";
				cx.stroke();
			} else if(colorScheme == 2) {
                if(M[i][j] != 0) {        
                    switch(M[i][j]) {
                        case 1:         // Driver Type-A: Not Chill
                            cR = 0; //41;
                            cG = 0; //128;
                            cB = 255; //185;
                            //rgb(41, 128, 185)
                        break;
                        case 2:         // Driver Type-B: So-so
                            cR = 0; //243;
                            cG = 255; //156;
                            cB = 0; //18;
                            //rgb(243, 156, 18)
                        break;
                        case 3:         // Driver Type-C: Chill
                            cR = 255; //39;
                            cG = 0; //174;
                            cB = 0; //96;
                            //rgb(39, 174, 96)
                        break;
                        default:
                    }
                    cx.fillStyle =
                        "rgb(" + cR + "," + cG + "," + cB + ")";
                    cx.beginPath();
                    //cx.fillRect(x, y, s, s);
										cx.arc(x + 0.5 * s, y + 0.5 * s, 0.5 * s, 0, 2 * Math.PI);
                    //cx.stroke();
										cx.fill();
										cx.closePath();
                }
            }
			cx.beginPath();
			cx.lineWidth = 1;
			cx.strokeStyle = "#eee";
			cx.rect(x, y, s, s);
			cx.stroke();
		}
	}
}

