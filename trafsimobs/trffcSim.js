// Get value of di and dj from a direction matrix
function getDirFromDirectionMatrix(i, j, D) {
	var di = 0;
	var dj = 0;
	var val = D[i][j]
	switch(val) {
			case 1:
					di = -1;
					dj = 0;
			break;
			case 2:
					di = -1;
					dj = 1;
			break;
			case 3:
					di = 0;
					dj = 1;
			break;
			case 4:
					di = 1;
					dj = 1;
			break;
			case 5:
					di = 1;
					dj = 0;
			break;
			case 6:
					di = 1;
					dj = -1;
			break;
			case 7:
					di = 0;
					dj = -1;
			break;
			case 8:
					di = -1;
					dj = -1;
			break;
			default:
			break;
	}
	return [di, dj];
}

